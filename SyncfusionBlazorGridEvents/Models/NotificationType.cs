namespace SyncfusionBlazorGridEvents.Models;
public enum NotificationType {
    
    /// <summary>
    /// Don't show this event in the UI
    /// </summary>
    None,

    /// <summary>
    /// Show only the event name in the UI
    /// </summary>
    NameOnly,
    
    /// <summary>
    /// Show both the event name and the event arguments in the UI
    /// </summary>
    NameAndArguments

}
